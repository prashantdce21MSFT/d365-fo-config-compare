    with open('/tmp/cdd_forms.json', encoding='utf-8') as f:
        data = json.load(f)

    results = []
    unknown = []

    for item in data['items']:
        form = item['form_name']
        wid  = item['id']
        mapping = lookup(form)
        # Fallback: ID-based lookup from full nav path
        if mapping is None and wid in KNOWN_BY_ID:
            mi, conf, note = KNOWN_BY_ID[wid]
            mapping = (mi, conf)
        mi, conf = mapping if mapping else (None, None)
        results.append({
            'id': wid,
            'title': item['title'],
            'form_name': form,
            'mi_name': mi or '',
            'confidence': conf or '',
            'state': item['state'],
            'needs_screenshot': mi is None
        })
        if mi is None and form.strip():
            unknown.append({'id': wid, 'form_name': form, 'title': item['title']})

    # Stats
    mapped   = sum(1 for r in results if r['mi_name'])
