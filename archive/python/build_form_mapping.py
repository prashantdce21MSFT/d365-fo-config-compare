def normalize(s):
    return re.sub(r'\s+', ' ', s.lower().strip())

def lookup(display_name):
    key = normalize(display_name)
    # Direct match
    if key in KNOWN:
        return KNOWN[key]
    # Strip trailing (MY) / (SG) / (MY60) etc and retry
    stripped = re.sub(r'\s*\((my|sg|my\d+|sg\d+)\)\s*$', '', key).strip()
    if stripped in KNOWN:
        return KNOWN[stripped]
    return None


